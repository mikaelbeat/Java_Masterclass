package Composition;

public class Dimensions {
	
	private int widht;
	private int height;
	private int depth;
	
	public Dimensions(int widht, int height, int depth){
		this.widht = widht;
		this.height = height;
		this.depth = depth;
	}
	
	public int getWidht(){
		return widht;
	}
	
	public int getHeight(){
		return height;
	}
	
	public int getDepth(){
		return depth;
	}

}
